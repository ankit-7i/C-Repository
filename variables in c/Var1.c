/*Variables:
Variable is a container is used to store the values in our
programs.
Variable is a named memory [bytes] location where we
can store and manipulate [ modify ] the values in our
programs.
Variables are always created in primary memory i.e.
RAM. Due to this once the program / function execution
completed, the variables are automatically deleted from
memory.
Variables are case sensitive. i.e. lower and upper are
different.
Eg:
Int a=10;
Int A=13;
Printf(“a=%d”,a);  10
Duplicate variables are not allowed in same function /
block.*/