/*goto label:
It is used to transfer program execution
from one place to another place [label].
In this process it is jumping from one area
to another without any condition. Hence it
is also called unconditional jumping
statement.



Here goto is a keyword.
Label is an identifier is used to identify the
area[line].
Every label should be end with : (colon)


Keywords not allowed in labels i.e. label
should be user defined.

Duplicate labels not allowed.
There is no space between go and to.
Label naming rules are similar to the
identifier rules.

Note: goto label working style is similar to
loops some times.

*/